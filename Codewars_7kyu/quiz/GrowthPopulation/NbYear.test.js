const nbYear = require('./NbYear');

test('nbYear(1500, 5, 100, 5000)는 15를 반환한다.', () => {
    expect(nbYear(1500, 5, 100, 5000)).toBe(15);
});

test('nbYear(1500000, 2.5, 10000, 2000000)는 10을 반환한다.', () => {
    expect(nbYear(1500000, 2.5, 10000, 2000000)).toBe(10);
});

test('nbYear(1500000, 0.25, 1000, 2000000)는 94를 반환한다.', () => {
    expect(nbYear(1500000, 0.25, 1000, 2000000)).toBe(94);
});
